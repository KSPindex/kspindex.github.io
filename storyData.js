function pickFieldLog(kind='general', key=null) {
  try {
    let arr = [];
    if (key && FieldLogDB[kind] && FieldLogDB[kind][key]) arr = FieldLogDB[kind][key];
    else arr = FieldLogDB[kind] || FieldLogDB.general;
    return arr[Math.floor(Math.random() * arr.length)] || '';
  } catch(e) { return ''; }
}

